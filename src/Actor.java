public class Actor {

  private String name;
  private String birthdate;

  public Actor(String theName, String theBirthdate) {

    this.name = theName;
    this.birthdate = theBirthdate;

  }

  public String getName() {

    return this.name;

  }

  public String getBirthdate() {

    return this.birthdate;

  }

}